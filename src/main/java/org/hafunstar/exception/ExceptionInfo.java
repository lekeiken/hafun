public class ExceptionInfo extends Exception{

    //储存提示信息
    private String message;

    public ExceptionInfo(String message) {
        super(message);
        this.message = message;
    }

    @Override
    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
